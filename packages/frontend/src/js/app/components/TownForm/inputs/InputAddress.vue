<template>
    <AutocompleteV2
        id="address"
        label=""
        prefixIcon="search"
        :search="autocomplete"
        :getResultValue="getResultValue"
        validationName="Adresse"
        @submit="submit"
        rules="required"
        data-cy-field="address"
        :defaultValue="value"
        :disabled="disabled"
    ></AutocompleteV2>
</template>

<script>
import { autocomplete } from "#helpers/addressHelper";

export default {
    props: {
        value: {
            value: Object,
            required: false,
            default() {
                return {
                    label: undefined,
                    citycode: undefined
                };
            }
        },
        disabled: {
            type: Boolean,
            required: false,
            default: false
        }
    },

    data() {
        return {
            input: this.value
        };
    },

    watch: {
        value() {
            this.input = this.value;
        },

        input() {
            this.$emit("input", this.input);
        }
    },

    methods: {
        autocomplete(str) {
            if (!str) {
                return [];
            }

            return autocomplete(str);
        },

        getResultValue(value) {
            if (typeof value === "string") {
                return value;
            }

            return value.label;
        },

        submit(result) {
            if (!result) {
                this.input = {
                    label: undefined,
                    citycode: undefined
                };
                this.$emit("change", undefined);
                return;
            }

            const {
                label,
                citycode,
                coordinates: [lng, lat]
            } = result;
            this.input = {
                label,
                citycode
            };
            this.$emit("change", [lat, lng]);
        }
    }
};
</script>
