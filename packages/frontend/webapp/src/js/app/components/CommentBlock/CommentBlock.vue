<template>
    <div
        class="border-t-1 border-G400 py-4"
        @mouseenter="isHover = true"
        @mouseleave="isHover = false"
    >
        <div class="flex justify-between">
            <div class="text-G600 text-sm mb-1">
                {{ formatDate(comment.createdAt, "d M y à h:i") }}
            </div>
            <span
                class="text-red font-bold cursor-pointer"
                v-if="showActionIcons && (isOwner || (canModerate && isHover))"
                @click="deleteMessage"
                >Supprimer {{ isOwner ? "mon" : "le" }} message
                <Icon icon="trash-alt" alt="Supprimer le message"
            /></span>
        </div>
        <div
            class="text-G600 text-sm mb-1"
            v-if="comment.covid && comment.covid.date"
        >
            Date de l'intervention:
            {{ formatDate(comment.covid.date, "d M y à h:i") }}
        </div>
        <div
            v-if="
                comment.user_target_name.length > 0 ||
                    comment.organization_target_name.length > 0
            "
        >
            <Icon icon="lock" class="text-red" />
            <span class="pl-1 font-bold"
                >Message réservé aux structures et utilisateurs suivants :</span
            >
            <div v-for="user in comment.user_target_name" :key="user">
                - {{ user }}
            </div>
            <div
                v-for="organization in comment.organization_target_name"
                :key="organization"
            >
                - {{ organization }}
            </div>
        </div>
        <div class="text-primary font-bold mb-1 mt-2">
            <router-link :to="`/annuaire/${comment.createdBy.organization_id}`">
                <Icon icon="user" /> {{ comment.createdBy.first_name }}
                {{ comment.createdBy.last_name }} -
                {{ comment.createdBy.organization }}
            </router-link>
        </div>
        <div class="ml-5">
            <div class="flex flex-wrap">
                <Tag
                    v-if="!!comment.covid"
                    variant="withoutBackground"
                    :class="['inline-block', 'bg-red', 'text-white']"
                    >Covid-19</Tag
                >
                <CovidTag
                    v-for="tag in covidTags"
                    :key="tag.prop"
                    :class="['mr-2', 'mb-2']"
                    :tag="tag"
                />
            </div>
            <div class="whitespace-pre-line">{{ comment.description }}</div>
            <RegularTag
                v-for="(tag, index) in regularTags"
                :key="index"
                :class="['mr-2', 'mb-2']"
                :tag="tag"
            />
            <div
                v-for="regularCommentTag in regularCommentTags"
                :key="regularCommentTag.uid"
            ></div>
        </div>
    </div>
</template>

<script>
import CovidTag from "#app/components/CovidTag/CovidTag.vue";
import RegularTag from "#app/components/RegularTag/RegularTag.vue";
import covidTags from "#app/pages/CovidHistory/covidTags";

export default {
    components: {
        CovidTag,
        RegularTag
    },
    props: {
        comment: {
            type: Object
        },
        // on moderation modal, the comment block is shown
        showActionIcons: {
            type: Boolean,
            default: false
        }
    },
    data() {
        const {
            regular_comment_tags: regularCommentTags
        } = this.$store.state.config.configuration;
        return {
            regularCommentTags,
            isHover: false
        };
    },
    methods: {
        /**
         * @see index.js
         */
        formatDate(...args) {
            return window.App.formatDate.apply(window, args);
        },

        deleteMessage() {
            this.$emit("moderate");
        }
    },
    computed: {
        covidTags: function() {
            if (!this.comment || !this.comment.covid) {
                return [];
            }

            return covidTags.filter(t => {
                return !!this.comment.covid[t.prop];
            });
        },
        regularTags: function() {
            if (!this.comment || !this.comment.tags) {
                return [];
            }
            return this.regularCommentTags.filter(tag =>
                this.comment.tags.includes(tag.uid)
            );
        },
        isOwner() {
            return (
                this.comment.createdBy.id ===
                this.$store.state.config.configuration.user.id
            );
        },
        canModerate() {
            return this.$store.getters["config/hasPermission"](
                "shantytown_comment.moderate"
            );
        }
    }
};
</script>
